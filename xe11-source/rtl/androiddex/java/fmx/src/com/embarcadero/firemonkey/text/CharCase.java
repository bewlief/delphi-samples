package com.embarcadero.firemonkey.text;

/**
 * Specifies what case is given to text typed into a control.
 */
public enum CharCase {

    /**
     * The text appears in mixed case. It is not forced into any case.
     */
    NORMAL,

    /**
     * The text is converted to uppercase.
     */
    UPPER_CASE,

    /**
     * The text is converted to lowercase.
     */
    LOWER_CASE
}